                                <img src="assets/img/news/bebeksinjay.jpg">
                                <p>Menjadi pulau yang terpisah dari daratan Jawa dan kini bisa disambangi melalui jembatan Suramadu, tentu kamu bakal lebih mudah mencapai pulau Madura. Gak cuma terkenal dengan sate khasnya, kalau kamu singgah di pulau tersebut, artinya kamu wajib kulineran di restoran Nasi Bebek Sinjay.</p>
                                <p>Berlokasi di Jalan Raya Ketengan No.45 kabupaten Bangkalan, restoran Bebek Sinjay memang hampir gak pernah sepi pembeli, terlebih menjelang jam makan siang.</p>
                                <p>Hmm, seperti apa sih cita rasa dari nasi Bebek Sinjay yang melegenda ini? Berikut ulasannya!</p>
                                <h5><strong>1. Tekstur Daging Bebek</strong></h5>
                                <img src="assets/img/kuliner/tekstur.jpg">
                                Disajikan di atas piring dengan nasi putih hangat, kamu akan menemukan tekstur daging bebek yang begitu lembut dan empuk. Saking lembutnya, daging bebek bisa lepas dari tulang dengan mudah.Seribu sayang, di kedai ini kamu gak bisa request potongan daging bebek bagian dada, sayap, atau paha, karena pelayan akan  memberikannya secara acak.</p>
                                <h5><strong>2. Soal rasa? Gurih tak tertahankan!</strong></h5>
                                <img src="assets/img/kuliner/tekstur2.jpg">
                                Kedai nasi Bebek Sinjay gak akan selalu ramai pembeli kalau cita rasanya gak menggoda. Sekali gigit saja, kamu bisa membuktikan kalau gurihnya Bebek Sinjay sungguh tak tertahankan. Bumbunya meresap sempurna dan menyebar ke dalam hingga bagian tulang. Mantap betul, bukan?</p>
                                <h5><strong>3. Lebih baik hindari jam makan siang</strong></h5>
                                <img src="assets/img/kuliner/tekstur3.jpg">
                                Terkenal hingga seantero Indonesia, kedai nasi Bebek Sinjay makin kebanjiran pembeli saat jam makan siang. Antreannya bisa mengular sangat panjang. Jadi, ada baiknya kamu berkunjung di luar jam makan siang supaya pesanan kamu lebih cepat datang.</p>
                                <h5><strong>4. Sudah buka cabang di banyak kota</strong></h5>
                                <img src="assets/img/kuliner/tekstur4.jpg">
                                Karena penggemar kuliner ini semakin lama semakin banyak, gak heran jika akhirnya nasi Bebek Sinjay membuka cabang di banyak kota, seperti Surabaya, Sidoarjo, Pandaan, Malang, dan Jakarta. Meski begitu, rasanya kurang afdol kalau belum merasakan sensasi makan nasi Bebek Sinjay langsung di kedai utamanya, Madura.</p>
                                <p>Nah, jadi penasaran kan dengan gurih dan cita rasa nasi Bebek Sinjay yang begitu istimewa ini? Kamu cuma perlu menembusnya dengan harga Rp29.000 per porsi nasi bebek lengkap dengan segelas es teh manis. Yuk, cobain!</p>
