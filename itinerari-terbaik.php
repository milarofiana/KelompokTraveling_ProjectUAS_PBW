                            <article class="mt-4 area-content">
                                <img src="assets/img/itinerary/IMG_20190616125411_6840.jpg">
                                <p>Apa yang terlintas di pikiran Teman Traveler ketika mendengar nama Kabupaten Bangkalan? Goa Pote? Jika memang begitu, kalian tak salah. Dalam beberapa tahun terakhir goa ini memang jadi objek wisata primadona di wilayah setempat. Pemandangan di sekitarnya sangat Instagramable, padahal hanya terbentuk dari bekas tambang kapur lho. Uniknya, beberapa kawasan di sekitarnya hingga kini masih berstatus lahan tambang aktif. Penasaran? Yuk, simak pengalaman saya jalan-jalan di Goa Pote.</p>
                                <h3><strong>Dekat Pusat Kota</strong></h3>
                                <img src="assets/img/itinerary/IMG_20190616125943_5627.jpg">
                                <p>Seperti disebutkan sebelumnya, dalam beberapa tahun terakhir Goa Pote jadi ikon wisata di Kabupaten Bangkalan. ‘Pote’ sendiri merupakan kata dalam Bahasa Madura yang artinya putih. Sebutan itu diberikan karena sekujur dinding goa ini memang bercorak putih.</p>
                                <p>Lantas di mana letak destinasi Instagenic ini? Teman Traveler bisa menemukannya di Desa Jaddih, Kecamatan Socah, Kabupaten Bangkalan. Masuk dalam sebuah kawasan bukit kapur luas dengan jarak kurang lebih 14 km dari Jembatan Suramadu. Jika berangkat dari Bangkalan, butuh waktu sekitar 30 menit untuk tiba di wisata Madura satu ini. Akses kendaraan menuju Goa Pote cukup mudah kok. Teman Traveler bisa menumpang kendaraan roda dua maupun empat. Jalanannya meski sedikit bergelombang, masih cukup aman dilalui.</p>
                                <h3><strong>Tambang Kapur Aktif</strong></h3>
                                <img src="assets/img/itinerary/IMG_20190616131127_7012.jpg">
                                <p>Goa Pote kini jadi salah satu daya tarik tersendiri bagi wisatawan yang berkunjung ke Bukit Jaddih. Dulunya tempat ini memang bukan objek wisata, namun dalam beberapa tahun terakhir mulai dibuka untuk umum. Padahal statusnya masih sebagai kawasan tambang kapur aktif lho.</p>
                                <p>Bekas guratan ‘tangan’ ekskavator di dinding Goa Pote menghasilkan pesona tersendiri. Di sini Teman Traveler bisa melihat pemandangan unik berupa deretan tebing tinggi berwarna putih, mengelilingi sebuah danau mungil.</p>
                                <p>Secara keseluruhan, panorama di sekitar sini memang cukup indah. Inilah alasan mengapa Goa Pote wajib masuk dalam list perjalanan kalian. Jika ingin mendapatkan panorama terbaik, sebaiknya Teman Traveler mampir saat pagi atau menjelang sore.</p>
                                <h3><strong>Objek Wisata Instagramable</strong></h3>
                                <img src="assets/img/itinerary/IMG_20190616133309_3614.jpg">
                                <p>Teman Traveler tentu sudah tak asing dengan pesona deretan tebing putih cantik plus danau mungil ala Goa Pote. Foto-foto keindahannya telah banyak tersebar di media sosial. Yap, hampir tiap sudut destinasi ini memang tampak Instagramable. Tak heran selalu jadi favorit mereka yang tengah liburan di Bangkalan.</p>
                                <p>Meski sinar mentari di kawasan ini bisa sangat terik di siang hari, animo wisatawan tak lantas surut. Para pengunjung tetap antusias datang untuk mengagumi guratan indah di tebing bekas tambang, danau kecil di depannya, tentunya sembari berfoto-foto.</p>
                                <p>Itulah sekilas ulasan mengenai Goa Pote, objek wisata instagramable di Kabupaten Bangkalan. Tunggu apa lagi, segera wujudkan rencana perjalanan ke Tanah Garam. Selamat liburan Teman Traveler. </p>
